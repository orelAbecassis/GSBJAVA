    private void btnValiderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnValiderMouseClicked
        
        if(cbxDosage.getSelectedItem().toString().compareTo("")==0)
        {
            JOptionPane.showMessageDialog(this, "Séléctionner une dose ");       
        }
        else if(cbxMed.getSelectedItem().toString().compareTo("")==0)
        {
            JOptionPane.showMessageDialog(this, "Séléctionner un medicament ");       
        }
        
        else if(txtPosologie.getText().compareTo("") == 0)
        {
            txtPosologie.setText("Veuillez saisir une Posologie svp ");
        }
        else
        {
            
            //c'est un objet et pour retourner un string .toString()
            fm.InsererUnePrescription(cbxMed.getSelectedItem().toString(),cbxIndiv.getSelectedItem().toString(),cbxDosage.getSelectedItem().toString(),txtPosologie.getText());
            //on affiche un message de validation 
            
            JOptionPane.showMessageDialog(this, "Votre Prescrition a bien été enregistré ");       
        }


        // TODO add your handling code here:
    }//GEN-LAST:event_btnValiderMouseClicked

